    private void jb_HabilitarPesosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jb_HabilitarPesosActionPerformed
        
        if(calcularNotasPoderadas.isCalculoTemPeso()){
            calcularNotasPoderadas.setCalculoTemPeso(false);
            jb_HabilitarPesos.setText("HABILITAR MÉDIA COM PESOS");
            jtf_Peso1.setEnabled(false);
            jtf_Peso2.setEnabled(false);
            jtf_Peso3.setEnabled(false);
            jtf_Peso4.setEnabled(false);
            jtf_Peso5.setEnabled(false);
             } else {
            calcularNotasPoderadas.setCalculoTemPeso(true);
            jb_HabilitarPesos.setText("DESABILITAR MÉDIA COM PESOS");
            jtf_Peso1.setEnabled(true);
            jtf_Peso2.setEnabled(true);
            if (CheckBox_Nota3.isSelected()) {
                jtf_Peso3.setEnabled(true);
            }
            if (CheckBox_Nota4.isSelected()) {
                jtf_Peso4.setEnabled(true);
            }
            if (CheckBox_Nota5.isSelected()) {
                jtf_Peso5.setEnabled(true);
            }
            
        }
    }//GEN-LAST:event_jb_HabilitarPesosActionPerformed

