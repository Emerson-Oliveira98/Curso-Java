    private void jb_CalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jb_CalcularActionPerformed
        calcularNotasPoderadas.setNota1(Double.parseDouble(jtf_Nota1.getText()));
        calcularNotasPoderadas.setNota2(Double.parseDouble(jtf_Nota2.getText()));

       // calcularNotasPoderadas.setPeso1(Double.parseDouble(jtf_Peso1.getText()));
       // calcularNotasPoderadas.setPeso2(Double.parseDouble(jtf_Peso2.getText()));
        
        if (CheckBox_Nota3.isSelected()) {
calcularNotasPoderadas.setNota3(Double.parseDouble(jtf_Nota3.getText()));
}
if (CheckBox_Nota4.isSelected()) {
calcularNotasPoderadas.setNota4(Double.parseDouble(jtf_Nota4.getText()));
}
if (CheckBox_Nota5.isSelected()) {
calcularNotasPoderadas.setNota5(Double.parseDouble(jtf_Nota5.getText()));
}
if (calcularNotasPoderadas.isCalculoTemPeso()) {
calcularNotasPoderadas.setPeso1(Double.parseDouble(jtf_Peso1.getText()));
calcularNotasPoderadas.setPeso2(Double.parseDouble(jtf_Peso2.getText()));
if (CheckBox_Nota3.isSelected()) {
calcularNotasPoderadas.setPeso3(Double.parseDouble(jtf_Peso3.getText()));
}
if (CheckBox_Nota4.isSelected()) {
calcularNotasPoderadas.setPeso4(Double.parseDouble(jtf_Peso4.getText()));
}
if (CheckBox_Nota5.isSelected()) {
calcularNotasPoderadas.setPeso5(Double.parseDouble(jtf_Peso5.getText()));
}

}        
        
        calcularNotasPoderadas.calcularAMedia();
        
         jtf_MediaNota.setText(df.format(calcularNotasPoderadas.getMediaFinal()));
         
         jtf_TotalDosPesos.setText(String.valueOf(calcularNotasPoderadas.getTotalDosPesos()));

    }//GEN-LAST:event_jb_CalcularActionPerformed

    private void jb_VerificarPesosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jb_VerificarPesosActionPerformed
        if (calcularNotasPoderadas.isCalculoTemPeso()){
            
    calcularNotasPoderadas.setTotalDosPesos(
Double.parseDouble(jtf_Peso1.getText()) +
Double.parseDouble(jtf_Peso2.getText()) +
Double.parseDouble(jtf_Peso3.getText()) +
Double.parseDouble(jtf_Peso4.getText()) +
Double.parseDouble(jtf_Peso5.getText()));

jtf_TotalDosPesos.setText(String.valueOf(
calcularNotasPoderadas.getTotalDosPesos()));

} else {
calcularNotasPoderadas.setTotalDosPesos(0);
jtf_TotalDosPesos.setText("");
}
    }//GEN-LAST:event_jb_VerificarPesosActionPerformed

