    private void BtnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnLoginActionPerformed
        if (verifica) {
            login = txtLogin.getText().toString();

            Usuarios user = new Usuarios();
            UsuariosDAO userDao = new UsuariosDAO();
            if (RbGerente.isSelected()) {
                try {
                    userDao.VerificarLoginGerente(user, login);

                    Senha = user.getSenha().toString();
                                   } catch (Exception e) {
                }

                if (txtSenha.getText().equals(Senha)) {
                    JOptionPane.showMessageDialog(null, "Login Efetuado com Sucesso");
                    this.dispose();
                    new  FrmPrincipal().setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(null, "Login ou Senha Incorretos");
                    txtSenha.setText("");
                    txtSenha.requestFocus();
                     this.dispose();
                    new  FrmPrincipal().setVisible(true);
                }
            }
            if (RbFuncionario.isSelected()) {
                try {
                    userDao.VerificarLoginFuncionario(user, login);

                    Senha = user.getSenha().toString();
                                   } catch (Exception e) {

                }

                if (txtSenha.getText().equals(Senha)) {
                    JOptionPane.showMessageDialog(null, "Login Efetuado com Sucesso");
                    this.dispose();
                    new  FrmPrincipal().setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(null, "Login ou Senha Incorretos");
                    txtSenha.setText("");
                    txtSenha.requestFocus();
                }
            }
        } else {
JOptionPane.showMessageDialog(null, "Por favor selecione algum cargo");
        }
    }//GEN-LAST:event_BtnLoginActionPerformed

