    private void txtSaborKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSaborKeyReleased
Sabores sb = new Sabores();
String sabor = txtSabor.getText();
SaboresDAO dao = new SaboresDAO();
        DefaultTableModel model = (DefaultTableModel)tblSabor.getModel();
  try {
            model.setRowCount(0);
        } catch (Exception e) {
        }
        dao.PesquisarPorNomeTamanhoTipo(sb, tblSabor, sabor,tamanho,tipo);
      


    }//GEN-LAST:event_txtSaborKeyReleased

    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        try {
       
        IdSabor=(Integer.parseInt(tblSabor.getValueAt(tblSabor.getSelectedRow(),0).toString()));
        NomeSabor=tblSabor.getValueAt(tblSabor.getSelectedRow(),1).toString();
        preco= Double.parseDouble(tblSabor.getValueAt(tblSabor.getSelectedRow(), 2).toString());
        tamanho = tblSabor.getValueAt(tblSabor.getSelectedRow(),3).toString();
        tipo = tblSabor.getValueAt(tblSabor.getSelectedRow(),4).toString();
        sabor = true;
        this.dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Por favor, selecione algum sabor","Selecionar Sabor",JOptionPane.OK_OPTION);
        }
        
    }//GEN-LAST:event_btnConfirmarActionPerformed

